create table if not exists Characters(
	id int GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
	"name" VARCHAR(50) NOT NULL,
	statistic_id int NULL,
 CONSTRAINT PK_Characters PRIMARY KEY (id)
);

create table if not exists Games(
	id int GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
	"name" VARCHAR(50) NOT NULL,
	start TIMESTAMP(3) NOT NULL,
	duration int NULL,
	game_type_id int NOT NULL,
	is_finished int NOT NULL,
 CONSTRAINT PK_Games PRIMARY KEY 
(
	id
)
);
create table if not exists GameTypeForbiddenItems(
	item_id int NOT NULL,
	game_type_id int NOT NULL,
 CONSTRAINT PK_GameTypeForbiddenItems PRIMARY KEY 
(
	item_id,
	game_type_id
)
);
create table if not exists GameTypes(
	id int GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
	"name" VARCHAR(50) NOT NULL,
	bonus_stats_id int NULL,
 CONSTRAINT PK_GameTypes PRIMARY KEY 
(
	id
)
);
create table if not exists Items(
	id int GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
	"name" VARCHAR(50) NULL,
	item_type_id int NOT NULL,
	statistic_id int NOT NULL,
	price money NOT NULL,
	min_level int NOT NULL,
 CONSTRAINT PK_Items PRIMARY KEY 
(
	id
)
);
create table if not exists ItemTypes(
	id int GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
	"name" VARCHAR(50) NOT NULL,
 CONSTRAINT PK_ItemTypes PRIMARY KEY 
(
	id
)
);
create table if not exists Statistics(
	id int GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
	strength int NOT NULL,
	defence int NOT NULL,
	mind int NOT NULL,
	speed int NOT NULL,
	luck int NOT NULL,
 CONSTRAINT PK_Statistics PRIMARY KEY 
(
	id
)
);
create table if not exists UserGameItems(
	item_id int NOT NULL,
	user_game_id int NOT NULL,
 CONSTRAINT PK_UserGameItems PRIMARY KEY 
(
	item_id,
	user_game_id
)
);
create table if not exists Users(
	id int GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
	username VARCHAR(50) NOT NULL,
	first_name VARCHAR(50) NULL,
	last_name VARCHAR(50) NULL,
	email VARCHAR(50) NULL,
	registration_date TIMESTAMP(3) NOT NULL,
	is_deleted SMALLINT NOT NULL,
	ip_address VARCHAR(15) NOT NULL,
 CONSTRAINT PK_Users PRIMARY KEY 
(
	id
)
);
create table if not exists UsersGames(
	id int GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
	game_id int NOT NULL,
	user_id int NOT NULL,
	character_id int NOT NULL,
	level int NOT NULL,
	joined_on TIMESTAMP(3) NOT NULL,
	cash money NOT NULL,
 CONSTRAINT PK_UsersGames_1 PRIMARY KEY 
(
	id
)
);

